    public boolean isFilesFull() {
        boolean resp = false;
        
        if (!txFieldName.getText().isEmpty() && !txFieldPassword.getText().isEmpty()) {
            resp = true;
        }
        
        return resp;
    }
    
    public boolean correctData() {
        boolean resp = false;
        
        if (!txFieldName.getText().equals("admin")) {
            if (!txFieldLastName.getText().equals("admin")) {
                if (!txFieldMLastName.getText().equals("admin")) {
                    resp = true;
                }
            }
        }
        
        return resp;
    }
    
